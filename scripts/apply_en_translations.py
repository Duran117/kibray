def main() -> int:
    text = PO.read_text(encoding="utf-8")
    applied = 0
    for src, dst in T.items():
        # Escape for regex; in .po, backslashes/quotes inside msgid are escaped.
        # We only need to escape backslashes and double-quotes for .po-literal form.
        po_src = src.replace("\\", "\\\\").replace('"', '\\"')
        po_dst = dst.replace("\\", "\\\\").replace('"', '\\"')
        # Match: msgid "X"\nmsgstr ""
        pattern = re.compile(
            rf'(msgid "{re.escape(po_src)}"\nmsgstr )""',
            re.MULTILINE,
        )
        new_text, n = pattern.subn(rf'\g<1>"{po_dst}"', text)
        if n:
            text = new_text
            applied += n
    PO.write_text(text, encoding="utf-8")
    print(f"Applied {applied} translation(s) to {PO.relative_to(PO.parents[3])}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
